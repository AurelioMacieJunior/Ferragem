    private void jTableMaterialMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMaterialMouseClicked

        String valor = ""+jTableMaterial.getValueAt(jTableMaterial.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from material where material_valor='"+valor+"'");
        try {
            conex.rs.first();
            jTextFieldValorPorUnidade.setText(String.valueOf(conex.rs.getFloat("material_valor")));
            jTextFieldId.setText(String.valueOf(conex.rs.getInt("material_id")));
            jTextFieldQuantidadeActual.setText(String.valueOf(conex.rs.getInt("material_quantidade")));

            jButtonCancelar.setEnabled(true);
            jButtonVender.setEnabled(true);
            jButtonConfirmarVenda.setEnabled(true);
            
            jTextFieldQuantidade.setEnabled(false);
            jTextFieldValorPorUnidade.setEnabled(false);
            //jDateChooserHoraDeVenda.setDateFormatString("");

        int quantidade;
        float preco;
        int valorPagar;

        quantidade = Integer.parseInt(jTextFieldQuantidade.getText());
        
        preco        = Float.parseFloat(jTextFieldValorPorUnidade.getText());
        valorPagar   = (int) (quantidade * preco) + 1000;

//        System.out.println(quantidade);
//        System.out.println(preco);
//        System.out.println(valorPagar);

        jTextFieldValorPagar.setText(""+valorPagar);
        
            preencherTabelaMaterial("select * from material order by material_nome");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERRO AO SELECIONAR" +ex);
        }
        conex.desconexao();
    }//GEN-LAST:event_jTableMaterialMouseClicked

