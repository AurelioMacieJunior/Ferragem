        public void preencherTabela(String sql){
            
        ArrayList dados = new ArrayList();
        String [] colunas = new String[]{"id","Nome","Contacto","Tipo De Carro","Matrícula"};
        conex.conexao();
        
        conex.executaSql(sql);
            try {
                conex.rs.first();
                do{
                  dados.add(new Object[]{conex.rs.getInt("pentrega_id"),conex.rs.getString("pentrega_nome"),conex.rs.getString("pentrega_contacto"),conex.rs.getString("pentrega_carro"),conex.rs.getString("pentrega_matricula")});  
                }while(conex.rs.next());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro Ao Preencher ArrayList\n " +ex);
            }
            
            ModeloTabela modelo = new ModeloTabela(dados, colunas);
            
            jTableProntaEntrega.setModel(modelo);
            jTableProntaEntrega.getColumnModel().getColumn(0).setPreferredWidth(30);
            jTableProntaEntrega.getColumnModel().getColumn(0).setResizable(false);
            jTableProntaEntrega.getColumnModel().getColumn(1).setPreferredWidth(250);
            jTableProntaEntrega.getColumnModel().getColumn(1).setResizable(false);
            jTableProntaEntrega.getColumnModel().getColumn(2).setPreferredWidth(180);
            jTableProntaEntrega.getColumnModel().getColumn(2).setResizable(false);
            jTableProntaEntrega.getColumnModel().getColumn(3).setPreferredWidth(190);
            jTableProntaEntrega.getColumnModel().getColumn(3).setResizable(false);
            jTableProntaEntrega.getColumnModel().getColumn(4).setPreferredWidth(130);
            jTableProntaEntrega.getColumnModel().getColumn(4).setResizable(false);
            jTableProntaEntrega.getTableHeader().setReorderingAllowed(false);
            jTableProntaEntrega.setAutoResizeMode(jTableProntaEntrega.AUTO_RESIZE_OFF);
            jTableProntaEntrega.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            conex.desconexao();
        }
