    private void jTableProntaEntregaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProntaEntregaMouseClicked

        String nome = ""+jTableProntaEntrega.getValueAt(jTableProntaEntrega.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select * from pentrega where pentrega_nome='"+nome+"'");
        try {
            conex.rs.first();
            jTextFieldId.setText(String.valueOf(conex.rs.getInt("pentrega_id")));
            jTextFieldNome.setText(String.valueOf(conex.rs.getString("pentrega_nome")));
            jComboBoxTipoDeCarro.setSelectedItem(String.valueOf(conex.rs.getString("pentrega_carro")));
            jTextFieldContacto.setText(String.valueOf(conex.rs.getString("pentrega_contacto")));
            jTextFieldMatricula.setText(String.valueOf(conex.rs.getString("pentrega_matricula")));

            jButtonExcluir.setEnabled(true);
            jButtonEditar.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            jButtonNovo.setEnabled(true);
            jButtonSalvar.setEnabled(false);

            jTextFieldNome.setEnabled(false);
            jComboBoxTipoDeCarro.setEnabled(false);
            jTextFieldContacto.setEnabled(false);
            jTextFieldMatricula.setEnabled(false);

            preencherTabela("select * from pentrega order by pentrega_id");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro Ao Seleccionar" +ex);
        }
        conex.desconexao();
    }//GEN-LAST:event_jTableProntaEntregaMouseClicked

