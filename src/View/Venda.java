        public void preencherProntaEntrega(){
        conex.conexao();
        conex.executaSql("select * from pentrega order by pentrega_nome");
        try {
            conex.rs.first();
            jComboBoxProntaEntrega.removeAllItems();
            do{
                jComboBoxProntaEntrega.addItem(conex.rs.getString("pentrega_nome"));
            }while(conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro Ao Preencher ComboBox");
        }
        conex.desconexao();
    }

