    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        int novaQuantidade;
        int actualQuantidade;
        int adicionar;
        String data;

        data = String.valueOf(jLabelDataDoSistema.getText());
        actualQuantidade = Integer.parseInt(jTextFieldQuantidade.getText());
        adicionar        = Integer.parseInt(jTextFieldAdicionar.getText());
        novaQuantidade   = adicionar + actualQuantidade;

        int codigo = Integer.parseInt(jTextFieldId.getText());
        dao.aumentar(novaQuantidade, data, codigo);
        preencherTabela("select * from material order by material_id");
        jTextFieldAdicionar.setText("");
        jTextFieldId.setText("");
        jTextFieldNomeDoMaterial.setText("");
        jTextFieldQuantidade.setText("");
        jTextFieldValorPorUnidade.setText("");
    }//GEN-LAST:event_jButton4ActionPerformed

